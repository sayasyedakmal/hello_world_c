/*
    Access modifier or format specifier
*/

#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("My favorite %s is %d.","number", 200);

    /*
        Access modifier

            %d = to print integer
            %c = to print character
            %s = to print string of character or array of character
            %f = to print floating point number
    */
    return 0;
}
